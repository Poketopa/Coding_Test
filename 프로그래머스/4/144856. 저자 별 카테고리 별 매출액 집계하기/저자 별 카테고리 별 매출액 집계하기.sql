SELECT
    A.AUTHOR_ID,
    A.AUTHOR_NAME,
    B.CATEGORY,
    sum(S.SALES * B.PRICE) AS TOTAL_SALES
FROM
    BOOK B
JOIN
    AUTHOR A
ON
    A.AUTHOR_ID = B.AUTHOR_ID
JOIN
    BOOK_SALES S
ON
    B.BOOK_ID = S.BOOK_ID
WHERE
    S.SALES_DATE LIKE '2022-01%'
GROUP BY
    A.AUTHOR_ID,
    A.AUTHOR_NAME,
    B.CATEGORY
ORDER BY
    A.AUTHOR_ID, B.CATEGORY DESC
